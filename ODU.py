# ============================================================
# ФУНКЦИЯ: прямой расчёт D(t) из данных
# ============================================================
def calc_D_direct(time_ch, mass_g, m_ob, rho_ob, S_func, rho_diff):
    """
    Прямой расчёт D(t) из экспериментальных данных.

    Формула: D(t) = m'(t) * m_об / (S(t)² * ρ_об * Δρ)

    Параметры:
        time_ch — массив времени (часы)
        mass_g — массив массы (г)
        m_ob — масса оболочки (г)
        rho_ob — плотность резины (г/см³)
        S_func — функция S(t), возвращает площадь в см²
        rho_diff — разность плотностей (г/см³)
    """
    # 1. Численная производная dm/dt (г/ч)
    # Используем центральные разности для внутренних точек
    dm_dt = np.gradient(mass_g, time_ch)  # г/ч

    # Переводим в г/с
    dm_dt_sec = dm_dt / 3600

    # 2. Площадь S(t) в каждой точке
    S_t = S_func(time_ch)

    # 3. Расчёт D(t)
    # D = m' * m_об / (S² * ρ_об * Δρ)
    D_t = (dm_dt_sec * m_ob) / (S_t ** 2 * rho_ob * rho_diff)

    return D_t, dm_dt, S_t


